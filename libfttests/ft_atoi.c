int		ft_atoi(char *str)
{
	int i;
	int a;
	int sign;

	i = 0;
	a = 0;
	sign = 1;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\r' || str[i] == '\v'
			|| str[i] == '\f' || str[i] == ' ')
				i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i]>= '0' && str[i] <= '9')
	{
		a = (a * 10) + (str[i] - 48);
		i++;
	}
	return (a * sign);
}
