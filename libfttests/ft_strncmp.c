int		ft_strncmp(char *s1, char *s2, unsigned int len)
{
	unsigned int i;

	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i] && i < len - 1)
		i++;
	return (s1[i] - s2[i]);
}
