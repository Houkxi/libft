#include "libft.h"

char	*ft_put_to_lower(char *s)
{
	int x;

	x = 0;
	while (s[x])
	{
		if (s[x] && s[x] >= 65 && s[x] <= 90)
			s[x] += 32;
		x++;
	}
	return (s);
}
