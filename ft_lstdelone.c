#include <string.h>
#include <stdlib.h>
#include "libft.h"

void	del(void *content, size_t content_size)
{
	free(content);
	content = NULL;
	content_size = 0;
}

void	ft_lstdelone(t_list **alst, void (*del)(void*, size_t))
{
	if (alst)
	{
		del((*alst)->content, (*alst)->content_size);
		free(*alst);
		*alst = NULL;
	}
}
