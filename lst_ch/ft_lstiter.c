#include <string.h>
#include <stdlib.h>
#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *tmp;

	tmp = lst;
	while (lst != NULL)
	{
		tmp = lst;
		lst = lst->next;
		f(tmp);
	}
}
